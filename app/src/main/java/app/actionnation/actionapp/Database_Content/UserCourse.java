package app.actionnation.actionapp.Database_Content;

public class UserCourse {
    String Edu_Id;
    int Challenge_Number;
    String Fb_Id;


    public UserCourse() {

    }

    public String getEdu_Id() {
        return Edu_Id;
    }

    public void setEdu_Id(String edu_Id) {
        Edu_Id = edu_Id;
    }

    public int getChallenge_Number() {
        return Challenge_Number;
    }

    public void setChallenge_Number(int challenge_Number) {
        Challenge_Number = challenge_Number;
    }

    public String getFb_Id() {
        return Fb_Id;
    }

    public void setFb_Id(String fb_Id) {
        Fb_Id = fb_Id;
    }
}
